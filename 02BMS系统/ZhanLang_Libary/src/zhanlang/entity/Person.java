package zhanlang.entity;


import java.io.Serializable;


public class Person implements Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = -5815724340776159144L;
	private String ID;
	private String name;
	private String sex;
	private String tel;
	private String age;
	private String address;
	private String userName;
	private String password;
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}

	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public String getName() {
		return name;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public Person() {
		super();
	}
	public Person(String id) {
		super();
		this.ID=id;
	}
	public Person(String userName, String password) {
		super();
		this.userName = userName;
		this.password = password;
	}
	
	public Person(String name, String sex, String tel, String age, String address, String userName, String password) {
		super();
		this.name = name;
		this.sex = sex;
		this.tel = tel;
		this.age = age;
		this.address = address;
		this.userName = userName;
		this.password = password;
	}
	
	@Override
	public String toString() {
		return "Person [ID=" + ID + ", name=" + name + ", sex=" + sex + ", tel=" + tel + ", age=" + age + ", address="
				+ address + ", userName=" + userName + ", password=" + password + "]";
	}
	
}
