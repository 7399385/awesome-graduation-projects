package com.v246;
public class NoLevelSortValues
{
	private String title;
	private String id;
	public NoLevelSortValues(String title,String id)
	{
		this.title=title;
		this.id=id;
	}
	public void setTitle(String title)
	{
		this.title=title;
	}
	public String getTitle()
	{
		return title;
	}
	public void setId(String id)
	{
		this.id=id;
	}
	public String getId()
	{
		return id;
	}
}